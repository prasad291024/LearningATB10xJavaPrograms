package Personal_Practice;

public class Print_Integers {
    public static void main(String[] arguments) {
        int c; //declaring an integer variable
        /* Using for loop to repeat instruction execution */
        for (c = 1; c <= 10; c++)           // initial value assigned to While_Num1to10 as 1
                                            // check current value of While_Num1to10 is less or equal to 10; While_Num1to10 increment;
        {
            System.out.println(c);          //print loop
        }
    }
}
